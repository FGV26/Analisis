    private void btonIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btonIniciarSesionActionPerformed
        String username = txtUsuario.getText();
        String password = new String(txtContraseña.getPassword());

        UsuarioDAO usuarioDAO = new UsuarioDAO();
        Usuario usuario = usuarioDAO.seleccionarPorUsername(username);

        if (usuario != null && usuario.getPassword().equals(password)) {
            switch (usuario.getRol()) {
                case "Vendedor":
                    new InterfazVendedor().setVisible(true);
                    break;
                case "Administrador":
                    new InterfazAdministrador().setVisible(true);
                    break;
                case "Proveedor":
                    new InterfazProveedor().setVisible(true);
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Rol no reconocido.");
                    break;
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos.");
        }
    }//GEN-LAST:event_btonIniciarSesionActionPerformed

