    private void verificarConexionBD() {
        if (!conexionVerificada) {
            boolean conexionExitosa = Conexion.verificarConexion();
            if (conexionExitosa) {
                JOptionPane.showMessageDialog(this, "Conexión a la base de datos exitosa.");
            } else {
                JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos.");
            }
            conexionVerificada = true;
        }
    }

