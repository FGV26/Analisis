    private void btonStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btonStockActionPerformed

        int selectedRow = tbleVehiculos.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un vehículo para solicitar stock.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tbleVehiculos.getModel();
        String marca = (String) model.getValueAt(selectedRow, 0);
        String modelo = (String) model.getValueAt(selectedRow, 1);
        String color = (String) model.getValueAt(selectedRow, 2);
        int stock = (int) model.getValueAt(selectedRow, 3);

        // Obtener el precio del vehículo
        VehiculoDAO vehiculoDAO = new VehiculoDAO();
        Vehiculo vehiculo = vehiculoDAO.obtenerPorMarcaModeloColor(marca, modelo, color);
        double precio = vehiculo != null ? vehiculo.getPrecio() : 0.0;

        SolicitarStock solicitarStock = new SolicitarStock(marca, modelo, color, stock, precio);
        solicitarStock.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btonStockActionPerformed

