    public void actualizarTablaVehiculos() {
        VehiculoDAO vehiculoDAO = new VehiculoDAO();
        List<Vehiculo> vehiculos = vehiculoDAO.seleccionarTodos();

        DefaultTableModel model = (DefaultTableModel) tbleVehiculos.getModel();
        model.setRowCount(0);
        for (Vehiculo vehiculo : vehiculos) {
            model.addRow(new Object[]{vehiculo.getMarca(), vehiculo.getModelo(), vehiculo.getColor(), vehiculo.getStock()});
        }
    }

